// -> Merge Sort (Not sorting in memory. If data is too big, performance is really important)

// #5 - Almost sorted Udemy review data needs to update and add 2 new reviews:
// -> Insertion Sort (Could be really fast when data is almost sorted)

// #6 - Temperature Records for the past 50 years in Canada
// -> Radix or Counting Sort
// -> Quick Sort (If the data is very accurate with decimals)

// #7 - Large user name database needs to be sorted. Data is very random
// -> Merge Sort (If we have enough memory)
// -> Quick Sort (If I'm not too worry about the worst case)

